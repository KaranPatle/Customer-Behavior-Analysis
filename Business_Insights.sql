-- 1. WHICH CATEGORY GENERATES HIGHEST REVENUE?

SELECT 
    category, ROUND(SUM(purchase_amount), 2) AS REVENUE
FROM
    customer_behaviour_analysis
GROUP BY category
ORDER BY REVENUE DESC;

-- Business Problem: Company does not know which category contributes most in the revenue

-- Impact:- 
			-- Helps prioritize high performing categories
			-- Optimizes inventory planning
            -- Improves marketing ROI
