-- 4. Which customers used discounts but still spent more than the average purchase amount?

SELECT 
    customer_id, discount_applied, purchase_amount
FROM
    customer_behaviour_analysis
WHERE
    discount_applied = 'Yes'
        AND purchase_amount > (SELECT 
            AVG(purchase_amount)
        FROM
            customer_behaviour_analysis)
ORDER BY purchase_amount DESC
LIMIT 10;

-- Business Problem:- Company cannot identify high-spending customers who are also discount users.

-- Impact:- 
		-- Identify premium discount-sensitive customers
        -- Enables targeted discount campaigns
        -- Improves customer retension and revenue
