-- 8. Top 5 products with highest discount usage

SELECT 
    item_purchased,
    COUNT(item_purchased) AS total_sold,
    COUNT(CASE
        WHEN discount_applied = 'Yes' THEN 1
    END) AS number_of_times_sold_when_discount_applied,
